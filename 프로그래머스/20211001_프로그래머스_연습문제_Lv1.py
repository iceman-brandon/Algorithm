# 같은 숫자는 싫어 문제
def solution(arr):
    answer = []
    for i in range(len(arr)):
        if i == 0:
            answer.append(arr[i])
        # 0번째 숫자는 무조건 정답 list에 추가    
        
        elif arr[i] != arr[i-1]:
            answer.append(arr[i])
        # 현재 arr 인덱스의 값이 바로 전 값과 다를 경우 :: 정답 list에 추가, 같을 경우 :: 추가 x    
    return answer
############ output ############
solution([1,1,3,3,0,1,1]) # [1, 3, 0, 1]
solution([4,4,4,3,3]) # [4, 3]

