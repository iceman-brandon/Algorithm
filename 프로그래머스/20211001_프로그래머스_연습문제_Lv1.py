# 2016년 문제
import datetime

def solution(a, b):
    days = ['MON', 'TUE', 'WED', 'THU', 'FRI', 'SAT', 'SUN']
    answer = days[datetime.date(2016, a, b).weekday()]
    # datetime 모듈의 weekday() 함수는 날짜의 요일 정보를 숫자값으로 리턴해준다 (0(월)~6(화)까지)
    return answer
############ output ############    
solution(5, 24) # 'TUE'

