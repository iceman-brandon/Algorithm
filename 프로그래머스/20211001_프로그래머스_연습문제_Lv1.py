# 두 정수 사이의 합 문제
def solution(a, b):
    answer = 0
    if a < b:
        for i in range(a, b+1):
            answer += i
        return answer
    
    else:
        return sum(list(range(b, a+1)))
        # ex) a=5, b=3일 경우 :: 3,4,5가 들어있는 리스트를 만들고 sum 한다.
############ output ############
solution(3, 5) # 12
solution(3, 3) # 3
solution(5, 3) # 12

